# 2Q.'''Create the below pattern using nested for loop in Python.'''

# In[8]:


for i in range(0, 4):
    for j in range(0, i+1):
        print('* ',end="")
    print()

for i in range(5,0,-1):
    for j in range(i):
        print('* ', end="")
    print('')

#3Q Write a Python program to reverse a word after accepting the input from the user.
# In[10]:


word = input("Input a word to reverse: ")

for char in range(len(word) - 1, -1, -1):
    print(word[char], end="")
print("\n")


